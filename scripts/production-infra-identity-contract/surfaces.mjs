function contentShape(contents) {
  return createHash("sha256").update(contents).digest("hex");
}

function providerKey(block) {
  return `${block.filePath}:provider.${block.labels[0]}.${stringAttribute(block, "alias") ?? "default"}`;
}

function resourceDataKey(block) {
  return block.kind === "resource" ? blockKey(block) : topLevelBlockKey(block);
}

function sensitiveVariableKey(block) {
  const moduleDirectory = block.filePath.slice(
    0,
    block.filePath.lastIndexOf("/"),
  );
  return `${moduleDirectory}:variable.${block.labels[0]}`;
}

function containsNonsensitiveCall(block) {
  return /\bnonsensitive(?:\s|\/\*[\s\S]*?\*\/|\/\/[^\r\n]*(?:\r?\n|$)|#[^\r\n]*(?:\r?\n|$))*\(/u.test(
    block.text,
  );
}

function validateExactInventory(
  blocks,
  registry,
  keyFor,
  label,
  errors,
  completeInventory,
) {
  const byKey = Map.groupBy(blocks, keyFor);
  const duplicates = [...byKey]
    .filter(([, matching]) => matching.length > 1)
    .map(([key]) => key)
    .sort();
  if (duplicates.length > 0) {
    errors.push(
      `${label} must be declared at most once: ${duplicates.join(", ")}`,
    );
  }
  const unregistered = new Set();
  for (const block of blocks) {
    const key = keyFor(block);
    const expected = registry.get(key);
    if (expected === undefined) {
      unregistered.add(key);
    } else {
      const actual = contentShape(block.text);
      if (actual !== expected) {
        errors.push(
          `${key}: ${label} must match its exact audited shape (found ${actual})`,
        );
      }
    }
  }
  if (unregistered.size > 0) {
    errors.push(
      `terraform: unregistered ${label} are forbidden: ${[...unregistered].sort().join(", ")}`,
    );
  }
  if (completeInventory) {
    const missing = [...registry.keys()]
      .filter((key) => !byKey.has(key))
      .sort();
    if (missing.length > 0) {
      errors.push(`${label} are missing: ${missing.join(", ")}`);
    }
  }
}

export function validateTerraformSourceSurfaces(
  blocks,
  errors,
  completeInventory = false,
) {
  const unsupported = blocks
    .filter((block) => !SUPPORTED_TOP_LEVEL_KINDS.has(block.kind))
    .map(topLevelBlockKey)
    .sort();
  if (unsupported.length > 0) {
    errors.push(
      `terraform: unsupported top-level blocks are forbidden: ${unsupported.join(", ")}`,
    );
  }

  const resourceDataBlocks = blocks.filter(
    (block) => block.kind === "resource" || block.kind === "data",
  );
  const byKey = Map.groupBy(resourceDataBlocks, resourceDataKey);
  const duplicates = [...byKey]
    .filter(([, matching]) => matching.length > 1)
    .map(([key]) => key)
    .sort();
  if (duplicates.length > 0) {
    errors.push(
      `Terraform resource/data blocks must be declared at most once: ${duplicates.join(", ")}`,
    );
  }
  const unregistered = resourceDataBlocks
    .map(resourceDataKey)
    .filter((key) => !EXPECTED_RESOURCE_DATA_BLOCK_KEYS.has(key))
    .sort();
  if (unregistered.length > 0) {
    errors.push(
      `terraform: unregistered Terraform resource/data blocks are forbidden: ${unregistered.join(", ")}`,
    );
  }
  if (completeInventory) {
    const missing = [...EXPECTED_RESOURCE_DATA_BLOCK_KEYS]
      .filter((key) => !byKey.has(key))
      .sort();
    if (missing.length > 0) {
      errors.push(
        `Terraform resource/data blocks are missing: ${missing.join(", ")}`,
      );
    }
  }

  validateExactInventory(
    blocks.filter((block) => block.kind === "terraform"),
    EXPECTED_TERRAFORM_BLOCK_SHAPES,
    topLevelBlockKey,
    "Terraform configuration blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter((block) => block.kind === "provider"),
    EXPECTED_PROVIDER_BLOCK_SHAPES,
    providerKey,
    "provider configuration blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter(
      (block) => block.kind === "resource" && block.type === "local_file",
    ),
    EXPECTED_LOCAL_MUTATION_BLOCK_SHAPES,
    blockKey,
    "local filesystem mutation blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter(
      (block) =>
        block.kind === "resource" &&
        IDENTITY_BEARING_RESOURCE_TYPES.has(block.type),
    ),
    EXPECTED_IDENTITY_BEARING_RESOURCE_BLOCK_SHAPES,
    blockKey,
    "identity-bearing resource blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter((block) => block.kind === "output"),
    EXPECTED_OUTPUT_BLOCK_SHAPES,
    topLevelBlockKey,
    "Terraform output blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter(containsNonsensitiveCall),
    EXPECTED_DECLASSIFICATION_BLOCK_SHAPES,
    (block) =>
      block.kind === "resource" ? blockKey(block) : topLevelBlockKey(block),
    "Terraform declassification blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter(
      (block) =>
        block.kind === "variable" &&
        EXPECTED_SENSITIVE_VARIABLE_BLOCK_SHAPES.has(
          sensitiveVariableKey(block),
        ),
    ),
    EXPECTED_SENSITIVE_VARIABLE_BLOCK_SHAPES,
    sensitiveVariableKey,
    "sensitive Terraform variable blocks",
    errors,
    completeInventory,
  );
  validateExactInventory(
    blocks.filter(
      (block) =>
        block.kind === "resource" && SECRET_PAYLOAD_SINK_TYPES.has(block.type),
    ),
    EXPECTED_SECRET_PAYLOAD_SINK_BLOCK_SHAPES,
    blockKey,
    "secret-payload sink blocks",
    errors,
    completeInventory,
  );

  const credentialBlocks = blocks
    .filter(
      (block) =>
        ["data", "ephemeral"].includes(block.kind) &&
        GOOGLE_CREDENTIAL_TYPES.has(block.labels[0]),
    )
    .map(topLevelBlockKey)
    .sort();
  if (credentialBlocks.length > 0) {
    errors.push(
      `terraform: Google credential-producing blocks are forbidden unless explicitly audited: ${credentialBlocks.join(", ")}`,
    );
  }
}
